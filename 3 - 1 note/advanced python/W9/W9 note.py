# matplotlib : 전체 패키지 이름
# pyplot : Matplotlib에 있는 최상위 모듈. 데이터 시각화를 위한 대부분의 기능을 제공

# figure : 그림이 나타나는 전체 창
# axes : 창에 들어가는 각각의 그래프 또는 그림

# 관련 홈페이지
# https://matplotlib.org/

# plot types
# plot() : 선 그래프
    # x축이 연속인 경우 사용함
    # ex: (연속적인) 시계열 데이터
# stem()
    # x축이 비연속적인 그래프
# bar(), barh() : 막대 차트
    # x축이 범주형 변수일 때 주로 사용함
# scatter() : 산점도
    # 두 데이터간의 관계를 볼 때 주로 사용
# hist() : 히스토그램
    # 표와 같은 내용의 분포를 시각화할 때 사용
# pie() : 파이 차트
    # 전체 대비 비율을 볼 때 주로 사용
# 기타 : boxplot, step, contour 등
